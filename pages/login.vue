<script setup>
definePageMeta({
  layout: false
})

useHead({
  title: 'Sign In - YourSite',
  meta: [
    {
      name: 'description',
      content: 'Sign in to your account to access your dashboard and manage your preferences.'
    }
  ]
})

const user = useSupabaseUser()
const supabase = useSupabaseClient()
const router = useRouter()

const form = ref({
  email: '',
  password: '',
  remember: false
})

const loading = ref(false)
const error = ref('')

// Redirect if already logged in
watch(user, (newUser) => {
  if (newUser) {
    router.push('/dashboard')
  }
}, { immediate: true })

const signIn = async () => {
  try {
    loading.value = true
    error.value = ''
    
    const { error: authError } = await supabase.auth.signInWithPassword({
      email: form.value.email,
      password: form.value.password
    })
    
    if (authError) throw authError
    
    // Redirect will happen automatically via the user watcher
  } catch (err) {
    error.value = err.message
  } finally {
    loading.value = false
  }
}

const signInWithProvider = async (provider) => {
  try {
    loading.value = true
    error.value = ''
    
    const { error: authError } = await supabase.auth.signInWithOAuth({
      provider: provider,
      options: {
        redirectTo: `${window.location.origin}/dashboard`
      }
    })
    
    if (authError) throw authError
  } catch (err) {
    error.value = err.message
    loading.value = false
  }
}
</script> 